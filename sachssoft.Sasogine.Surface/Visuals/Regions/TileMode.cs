namespace sachssoft.Sasogine.Surface.Visuals.Regions
{
    /// <summary>
    /// Defines how a <see cref="TiledRegion"/> repeats its texture.
    /// </summary>
    public enum TileMode
    {
        /// <summary>
        /// Repeat the tile horizontally only.
        /// </summary>
        X,

        /// <summary>
        /// Repeat the tile vertically only.
        /// </summary>
        Y,

        /// <summary>
        /// Repeat the tile in both horizontal and vertical directions.
        /// </summary>
        XY
    }
}
