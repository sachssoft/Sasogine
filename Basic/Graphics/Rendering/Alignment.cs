namespace Sachssoft.Sasogine.Graphics.Rendering
{
    /// <summary>
    /// Defines the alignment position of an element relative to an axis or reference point.
    /// </summary>
    public enum Alignment
    {
        /// <summary>
        /// Aligns the element to the near side or start position.
        /// </summary>
        Near,

        /// <summary>
        /// Aligns the element to the center position.
        /// </summary>
        Center,

        /// <summary>
        /// Aligns the element to the far side or end position.
        /// </summary>
        Far
    }
}