namespace Sachssoft.Sasogine.Graphics.Rendering
{
    /// <summary>
    /// Defines the direction or axis in which a position-related operation is applied.
    /// </summary>
    public enum PositionDirection
    {
        /// <summary>
        /// Applies the operation along the horizontal axis.
        /// </summary>
        Horizontal,

        /// <summary>
        /// Applies the operation along the vertical axis.
        /// </summary>
        Vertical,

        /// <summary>
        /// Applies the operation along both horizontal and vertical axes.
        /// </summary>
        Both
    }
}